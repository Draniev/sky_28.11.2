def capitalize_str(string: str) -> str:
    """
    Функция преобразует входящую строку в заглавные буквы

    Parameters:
    string: str
        Строка для преобразования
    """
    return string.capitalize()


def title_str(string: str) -> str:
    """
    Делает заглавными первую буквы каждого слова входящей строки

    Parameters:
    string: str
        Строка для преобразования
    """
    return string.title()
